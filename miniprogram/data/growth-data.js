/**
 * 获取最近的数据点（用于插值）
 * @param {string} gender - 'boy' 或 'girl'
 * @param {number} monthAge - 月龄
 * @param {string} type - 'height' 或 'weight'
 */
function getNearestData(gender, monthAge, type) {
  const data = GROWTH_DATA[gender];
  const keys = Object.keys(data).map(Number).sort((a, b) => a - b);
  
  // 边界检查
  if (monthAge <= keys[0]) return data[keys[0]][type === 'height' ? 0 : 1];
  if (monthAge >= keys[keys.length - 1]) return data[keys[keys.length - 1]][type === 'height' ? 0 : 1];
  
  // 找到最近的上下界
  let lower = keys[0], upper = keys[0];
  for (let key of keys) {
    if (key <= monthAge) lower = key;
    if (key >= monthAge && key < upper) upper = key;
  }
  
  // 如果正好匹配
  if (lower === upper || upper === keys[0]) {
    return data[lower][type === 'height' ? 0 : 1];
  }
  
  // 线性插值
  const ratio = (monthAge - lower) / (upper - lower);
  const lowerData = data[lower][type === 'height' ? 0 : 1];
  const upperData = data[upper][type === 'height' ? 0 : 1];
  
  return lowerData.map((val, i) => val + (upperData[i] - val) * ratio);
}

/**
 * 计算精确百分位
 * @param {string} gender - 'boy' 或 'girl'
 * @param {number} monthAge - 月龄
 * @param {number} value - 实际测量值
 * @param {string} type - 'height' 或 'weight'
 * @returns {object} - {percentile, interpolated, grade, range}
 */
function calculatePercentile(gender, monthAge, value, type) {
  const percentiles = getNearestData(gender, monthAge, type);
  
  // 百分位值: [P3, P10, P25, P50, P75, P90, P97]
  const percentileKeys = [3, 10, 25, 50, 75, 90, 97];
  
  let result = {};
  
  if (value <= percentiles[0]) {
    result = { percentile: '<P3', interpolated: 3, grade: '偏低' };
  } else if (value >= percentiles[6]) {
    result = { percentile: '>P97', interpolated: 97, grade: '偏高' };
  } else {
    // 找到value所在的区间
    for (let i = 0; i < percentiles.length - 1; i++) {
      if (value >= percentiles[i] && value <= percentiles[i + 1]) {
        // 线性插值计算精确百分位
        const ratio = (value - percentiles[i]) / (percentiles[i + 1] - percentiles[i]);
        const interpolated = percentileKeys[i] + (percentileKeys[i + 1] - percentileKeys[i]) * ratio;
        
        // 确定等级
        let grade = '中等';
        if (interpolated < 10) grade = '下等';
        else if (interpolated < 25) grade = '中下';
        else if (interpolated < 75) grade = '中等';
        else if (interpolated < 90) grade = '中上';
        else grade = '上等';
        
        result = { 
          percentile: `P${Math.round(interpolated)}`, 
          interpolated: Math.round(interpolated * 10) / 10,
          grade,
          range: [percentiles[i], percentiles[i + 1]]
        };
        break;
      }
    }
  }
  
  result.referenceValues = {
    p3: percentiles[0].toFixed(1),
    p10: percentiles[1].toFixed(1),
    p25: percentiles[2].toFixed(1),
    p50: percentiles[3].toFixed(1),
    p75: percentiles[4].toFixed(1),
    p90: percentiles[5].toFixed(1),
    p97: percentiles[6].toFixed(1)
  };
  
  return result;
}

/**
 * 计算BMI百分位
 */
function calculateBMIPercentile(gender, monthAge, height, weight) {
  const heightM = height / 100;
  const bmi = weight / (heightM * heightM);
  return bmi;
}

